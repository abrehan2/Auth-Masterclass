// IMPORTS -
import { db } from "@/lib/db";

// GET USER BY EMAIL -
export const getUserByEmail = async (email: string) => {
  try {
    const user = await db.user.findUnique({
      where: {
        email,
      },
    });

    return user;
  } catch {
    return null;
  }
};

